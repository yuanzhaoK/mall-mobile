  Widget _buildUserInfoCard() => GestureDetector(
    onTap: () {
      if (!isLoggedIn) {
        _showLoginBottomSheet();
      } else {
        _navigateToProfileEdit();
      }
    },
    child: Card(
      elevation: 4,
      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
      child: Padding(
        padding: const EdgeInsets.all(20),
        child: Row(
          children: [
            CircleAvatar(
              radius: 40,
              backgroundColor: AppColors.primary,
              backgroundImage: isLoggedIn && currentUser?.avatar != null
                  ? NetworkImage(currentUser!.avatar!)
                  : null,
              child: isLoggedIn && currentUser?.avatar == null
                  ? Text(
                      (currentUser?.username.isNotEmpty ?? false)
                          ? currentUser!.username[0].toUpperCase()
                          : (username.isNotEmpty
                                ? username[0].toUpperCase()
                                : 'U'),
                      style: const TextStyle(
                        fontSize: 24,
                        fontWeight: FontWeight.bold,
                        color: Colors.white,
                      ),
                    )
                  : isLoggedIn == false
                  ? const Icon(Icons.person, size: 40, color: Colors.white)
                  : null,
            ),
            const SizedBox(width: 20),
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    isLoggedIn ? (currentUser?.username ?? username) : 'ÁÇπÂáªÁôªÂΩï',
                    style: TextStyle(
                      fontSize: 20,
                      fontWeight: FontWeight.bold,
                      color: isLoggedIn
                          ? AppColors.textPrimary
                          : AppColors.textSecondary,
                    ),
                  ),
                  const SizedBox(height: 8),
                  Container(
                    padding: const EdgeInsets.symmetric(
                      horizontal: 12,
                      vertical: 4,
                    ),
                    decoration: BoxDecoration(
                      color: AppColors.primary.withValues(alpha: 0.1),
                      borderRadius: BorderRadius.circular(12),
                    ),
                    child: Builder(
                      builder: (context) {
                        final memberText = isLoggedIn
                            ? (memberLevel?.displayName ?? AppStrings.vipMember)
                            : 'Êú™ÁôªÂΩïÁî®Êà∑';
                        debugPrint('üîê UIÊòæÁ§∫‰ºöÂëòÊñáÊú¨: $memberText');
                        return Text(
                          memberText,
                          style: TextStyle(
                            fontSize: 14,
                            color: isLoggedIn
                                ? AppColors.primary
                                : AppColors.textSecondary,
                            fontWeight: FontWeight.w500,
                          ),
                        );
                      },
                    ),
                  ),
                ],
              ),
            ),
            Icon(
              isLoggedIn ? Icons.edit : Icons.arrow_forward_ios,
              color: AppColors.textSecondary,
              size: 16,
            ),
          ],
        ),
      ),
    ),
  );

  /// ÊûÑÂª∫‰ºöÂëòËØ¶ÊÉÖÂç°Áâá
  Widget _buildMemberInfoCard() {
    if (memberLevel == null) return const SizedBox.shrink();

    return Card(
      elevation: 2,
      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
      child: DecoratedBox(
        decoration: BoxDecoration(
          borderRadius: BorderRadius.circular(16),
          gradient: LinearGradient(
            colors: [
              _getMemberColor(memberLevel!.color),
              _getMemberColor(memberLevel!.color).withValues(alpha: 0.7),
            ],
            begin: Alignment.topLeft,
            end: Alignment.bottomRight,
          ),
        ),
        child: Padding(
          padding: const EdgeInsets.all(20),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              // ‰ºöÂëòÁ≠âÁ∫ßÊ†áÈ¢ò
              Row(
                children: [
                  const Icon(Icons.star, color: Colors.white, size: 24),
                  const SizedBox(width: 8),
                  Text(
                    memberLevel!.displayName,
                    style: const TextStyle(
                      fontSize: 20,
                      fontWeight: FontWeight.bold,
                      color: Colors.white,
                    ),
                  ),
                  const Spacer(),
                  Text(
                    'Lv.${memberLevel!.level}',
                    style: const TextStyle(
                      fontSize: 16,
                      fontWeight: FontWeight.w600,
                      color: Colors.white,
                    ),
                  ),
                ],
              ),
              if (memberLevel!.slogan != null) ...[
                const SizedBox(height: 8),
                Text(
                  memberLevel!.slogan!,
                  style: const TextStyle(
                    fontSize: 14,
                    color: Colors.white,
                    fontStyle: FontStyle.italic,
                  ),
                ),
              ],
              const SizedBox(height: 16),

              // ‰ºöÂëòÊùÉÁõä
              if (memberLevel!.benefits != null) _buildMemberBenefits(),

              // ‰ºöÂëò‰ø°ÊÅØ
              const SizedBox(height: 16),
              _buildMemberStats(),
            ],
          ),
        ),
      ),
    );
  }

  /// ÊûÑÂª∫‰ºöÂëòÊùÉÁõä
  Widget _buildMemberBenefits() {
    final benefits = memberLevel!.benefits!;

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        const Text(
          '‰ºöÂëòÊùÉÁõä',
          style: TextStyle(
            fontSize: 16,
            fontWeight: FontWeight.w600,
            color: Colors.white,
          ),
        ),
        const SizedBox(height: 8),
