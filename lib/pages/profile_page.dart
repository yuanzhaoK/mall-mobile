          spacing: 8,
          runSpacing: 4,
          children: [
            // ÊäòÊâ£ÊùÉÁõä
            ...?benefits.discounts?.map(_buildBenefitChip),
            // ÁâπÊùÉ
            ...?benefits.privileges?.map(_buildBenefitChip),
            // ÊúçÂä°
            ...?benefits.services?.map(_buildBenefitChip),
          ],
        ),
      ],
    );
  }

  /// ÊûÑÂª∫ÊùÉÁõäÊ†áÁ≠æ
  Widget _buildBenefitChip(String text) {
    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 4),
      decoration: BoxDecoration(
        color: Colors.white.withValues(alpha: 0.2),
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: Colors.white.withValues(alpha: 0.3)),
      ),
      child: Text(
        text,
        style: const TextStyle(
          fontSize: 12,
          color: Colors.white,
          fontWeight: FontWeight.w500,
        ),
      ),
    );
  }

  /// ÊûÑÂª∫‰ºöÂëòÁªüËÆ°‰ø°ÊÅØ
  Widget _buildMemberStats() {
    return Row(
      children: [
        if (memberLevel!.discountRate != null)
          Expanded(
            child: _buildStatItem(
              'ÊäòÊâ£Áéá',
              '${((1 - memberLevel!.discountRate!) * 100).toStringAsFixed(1)}Êäò',
            ),
          ),
        if (memberLevel!.pointsRate != null)
          Expanded(
            child: _buildStatItem(
              'ÁßØÂàÜÂÄçÁéá',
              '${memberLevel!.pointsRate!.toStringAsFixed(1)}x',
            ),
          ),
        if (currentUser?.points != null)
          Expanded(child: _buildStatItem('ÂΩìÂâçÁßØÂàÜ', '${currentUser!.points}')),
      ],
    );
  }

  /// ÊûÑÂª∫ÁªüËÆ°È°π
  Widget _buildStatItem(String label, String value) {
    return Column(
      children: [
        Text(
          value,
          style: const TextStyle(
            fontSize: 18,
            fontWeight: FontWeight.bold,
            color: Colors.white,
          ),
        ),
        const SizedBox(height: 4),
        Text(
          label,
          style: const TextStyle(
            fontSize: 12,
            color: Colors.white,
            fontWeight: FontWeight.w400,
          ),
        ),
      ],
    );
  }

  /// Ëé∑Âèñ‰ºöÂëòÁ≠âÁ∫ßÈ¢úËâ≤
  Color _getMemberColor(String? color) {
    if (color == null) return AppColors.primary;

    try {
      // Â§ÑÁêÜÂçÅÂÖ≠ËøõÂà∂È¢úËâ≤ÂÄº
      if (color.startsWith('#')) {
        return Color(int.parse(color.replaceFirst('#', '0xFF')));
      }
      return AppColors.primary;
    } catch (e) {
      return AppColors.primary;
    }
  }

