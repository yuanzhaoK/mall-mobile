  void _showLoginBottomSheet() {
    showModalBottomSheet(
      context: context,
      isScrollControlled: true,
      backgroundColor: Colors.transparent,
      builder: (context) => LoginDialog(
        onLoginSuccess: (authResponse) async {
          debugPrint('üîê ProfilePageÊî∂Âà∞ÁôªÂΩïÊàêÂäüÂõûË∞É: ${authResponse.user.username}');

          // ‰øùÂ≠òÁî®Êà∑Êï∞ÊçÆÂà∞Êú¨Âú∞Â≠òÂÇ®
          await UserStorage.saveUserData(
            user: authResponse.user,
            memberLevel: authResponse.memberLevel,
          );

          setState(() {
            isLoggedIn = true;
            currentUser = authResponse.user;
            username = authResponse.user.username;
            memberLevel = authResponse.memberLevel;
            isLoading = false;
          });
          debugPrint(
            'üîê ProfilePageÁä∂ÊÄÅÂ∑≤Êõ¥Êñ∞: isLoggedIn=$isLoggedIn, username=$username',
          );
          debugPrint('üîê ‰ºöÂëòÁ≠âÁ∫ß‰ø°ÊÅØ: ${memberLevel?.displayName ?? "Êó†Á≠âÁ∫ß‰ø°ÊÅØ"}');
        },
      ),
    );
  }

  /// ÂØºËà™Âà∞‰∏™‰∫∫‰ø°ÊÅØÁºñËæëÈ°µÈù¢
  void _navigateToProfileEdit() {
    if (currentUser != null) {
      Navigator.push(
        context,
        MaterialPageRoute(
          builder: (context) => ProfileEditPage(user: currentUser!),
        ),
      ).then((result) {
        if (result != null && result is User) {
          setState(() {
            currentUser = result;
            username = result.username;
          });
        }
      });
    }
  }

  Future<void> _logout() async {
    try {
      // ÊòæÁ§∫Âä†ËΩΩÁä∂ÊÄÅ
      setState(() {
        isLoading = true;
      });

      // Ë∞ÉÁî®GraphQLÊ≥®ÈîÄAPI
      await GraphQLService.logout();

      // Ê∏ÖÈô§Áî®Êà∑Êï∞ÊçÆÔºà‰øùÁïôËÆ∞‰ΩèÁöÑË¥¶Âè∑ÂØÜÁ†ÅÔºâ
      await UserStorage.clearUserData();
      debugPrint('üîê ÈÄÄÂá∫ÁôªÂΩïÔºöÂ∑≤Ê∏ÖÈô§Áî®Êà∑Êï∞ÊçÆÔºå‰øùÁïôËÆ∞‰ΩèÁöÑË¥¶Âè∑ÂØÜÁ†Å');

      // Êõ¥Êñ∞Êú¨Âú∞Áä∂ÊÄÅ
      setState(() {
        isLoggedIn = false;
        currentUser = null;
        memberLevel = null;
        username = AppStrings.username;
        isLoading = false;
      });

      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Â∑≤ÈÄÄÂá∫ÁôªÂΩï'), backgroundColor: Colors.orange),
      );
    } catch (e) {
      setState(() {
        isLoading = false;
      });

      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('ÈÄÄÂá∫ÁôªÂΩïÂ§±Ë¥•: ${e.toString()}'),
          backgroundColor: Colors.red,
        ),
      );
    }
  }
