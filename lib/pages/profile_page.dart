class _ProfilePageState extends State<ProfilePage> {
  bool isLoggedIn = false;
  String username = AppStrings.username;
  User? currentUser;
  MemberLevel? memberLevel;
  bool isLoading = false;

  @override
  void initState() {
    super.initState();
    _checkLoginStatus();
  }

  Future<void> _checkLoginStatus() async {
    try {
      // È¶ñÂÖàÊ£ÄÊü•Êú¨Âú∞Â≠òÂÇ®ÁöÑÁî®Êà∑Êï∞ÊçÆ
      final userData = await UserStorage.getUserData();
      final hasToken = GraphQLService.isLoggedIn;

      if (userData != null && hasToken) {
        // ÊúâÊú¨Âú∞Êï∞ÊçÆ‰∏îÊúâtokenÔºåÊÅ¢Â§çÁî®Êà∑Áä∂ÊÄÅ
        setState(() {
          isLoggedIn = true;
          currentUser = userData.user;
          username = userData.user.username;
          memberLevel = userData.memberLevel;
        });
        debugPrint('üîê Â∑≤ÊÅ¢Â§çÁî®Êà∑Áä∂ÊÄÅ: ${userData.user.username}');
        debugPrint('üîê Â∑≤ÊÅ¢Â§ç‰ºöÂëòÁ≠âÁ∫ß: ${userData.memberLevel?.displayName ?? "Êó†"}');
      } else if (hasToken) {
        // Êúâtoken‰ΩÜÊó†Êú¨Âú∞Êï∞ÊçÆÔºåÂ∞ùËØï‰ªéÊúçÂä°Âô®Ëé∑Âèñ
        setState(() {
          isLoggedIn = true;
        });

        try {
          final user = await GraphQLService.getCurrentUser();
          if (user != null) {
            setState(() {
              currentUser = user;
              username = user.username;
            });
            // ‰øùÂ≠òÂà∞Êú¨Âú∞Â≠òÂÇ®
            await UserStorage.saveUserData(user: user);
          }
        } catch (e) {
          debugPrint('‰ªéÊúçÂä°Âô®Ëé∑ÂèñÁî®Êà∑‰ø°ÊÅØÂ§±Ë¥•: $e');
          // Â¶ÇÊûúËé∑ÂèñÂ§±Ë¥•ÔºåÊ∏ÖÈô§tokenÂíåÊú¨Âú∞Êï∞ÊçÆ
          await GraphQLService.logout();
          await UserStorage.clearUserData();
          setState(() {
            isLoggedIn = false;
            currentUser = null;
            memberLevel = null;
            username = AppStrings.username;
          });
        }
      } else {
        // Êó†tokenÔºåÁ°Æ‰øùÊ∏ÖÈô§Êú¨Âú∞Êï∞ÊçÆ
        await UserStorage.clearUserData();
        setState(() {
          isLoggedIn = false;
          currentUser = null;
          memberLevel = null;
          username = AppStrings.username;
        });
      }
    } catch (e) {
      debugPrint('Ê£ÄÊü•ÁôªÂΩïÁä∂ÊÄÅÂ§±Ë¥•: $e');
      setState(() {
        isLoggedIn = false;
        currentUser = null;
        memberLevel = null;
        username = AppStrings.username;
      });
    }
  }

  @override
  Widget build(BuildContext context) {
    debugPrint(
      'üîê ProfilePage build - isLoggedIn: $isLoggedIn, username: $username',
    );
    debugPrint('üîê ProfilePage build - memberLevel: $memberLevel');
    debugPrint(
      'üîê ProfilePage build - memberLevel?.displayName: ${memberLevel?.displayName}',
    );
    return Scaffold(
      appBar: AppBar(
        title: const Text(AppStrings.tabProfile),
        backgroundColor: AppColors.primary,
        foregroundColor: Colors.white,
        centerTitle: true,
        elevation: 2,
      ),
      backgroundColor: AppColors.background,
